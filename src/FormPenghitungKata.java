    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try {
            JFileChooser chooser = new JFileChooser();
            if (chooser.showSaveDialog(this) == JFileChooser.APPROVE_OPTION) {
                File file = chooser.getSelectedFile();
                try (BufferedWriter bw = new BufferedWriter(new FileWriter(file))) {
                    bw.write("Isi Teks:\n" + txtAreaTeks.getText() + "\n\n");
                    bw.write("Jumlah Kata: " + lblKata.getText() + "\n");
                    bw.write("Jumlah Karakter: " + lblKarakter.getText() + "\n");
                    bw.write("Jumlah Kalimat: " + lblKalimat.getText() + "\n");
                    bw.write("Jumlah Paragraf: " + lblParagraf.getText() + "\n");
                }
                JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
        String teks = txtAreaTeks.getText().toLowerCase();
        String cari = txtCari.getText().toLowerCase();

        if (cari.isEmpty()) {
            lblHasilCari.setText("Masukkan kata yang ingin dicari!");
            return;
        }

        int jumlah = 0;
        for (String word : teks.split("\\s+")) {
            if (word.equals(cari)) jumlah++;
        }

        lblHasilCari.setText("Kata \"" + cari + "\" ditemukan " + jumlah + " kali.");
    }//GEN-LAST:event_btnCariActionPerformed

